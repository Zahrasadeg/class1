package class2;

public class Math {
    public static void main(String[] args) {
        float a=10.2f;
        float b=2.2f;
        System.out.println("the addition of "+a+" and "+b+" is equal to "+(a+b));
        System.out.println("the subtraction of "+a+" and "+b+" is equal to "+(a-b));
        System.out.println("the multiplication of "+a+" and "+b+" is equal to "+(a*b));
        System.out.println("the division of "+a+" and "+b+" is equal to "+(a/b));
        float c=5.5f;
        System.out.println("the square of "+c+" is equal to "+(c*c));


    }
}
